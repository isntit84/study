package step11;

public class Person {
	void study(String subject) {

		System.out.println(subject + "공부하다");

	}

	void wake(int time) {
		System.out.println(time + "시 기상!");
	}

	void eat(String food, int count) {
		System.out.println(food + " " + count + "인분 먹다");
	}

	void plus(int i, int j) {
		System.out.println(i + j);
	}
}
